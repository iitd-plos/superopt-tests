#include <stdbool.h>
#include <stdlib.h>

bool fcmp(double x, double y)
{
  return x < y;
}

double fadd(double x, double y)
{
  return x + y;
}

double fsub(double x, double y)
{
  return x - y;
}

double fmul(double x, double y)
{
  return x * y;
}

double fdiv(double x, double y)
{
  return x / y;
}
